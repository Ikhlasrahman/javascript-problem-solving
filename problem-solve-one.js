/** Take two variables price1 and price2,
 * and compare them using all comparison operators
 *  (==, ===, !=, !==, >, <, >=, <=), printing each result with console.log. */

const price1 = 50;
const price2 = 100;

console.log(price1 == price2);
console.log(price1 === price2);
console.log(price1 != price2);
console.log(price1 !== price2);
console.log(price1 > price2);
console.log(price1 < price2);
console.log(price1 >= price2);
console.log(price1 <= price2);